
// Java program to multiply two floating point numbers

package java_package;

public class Multiply_FloatingPoint_Numbers {

	public static void main(String[] args) {

		float f1 = 2f;
		float f2= 9.7f;
		
		float product = f1*f2;
		
		System.out.println("Product of f1 and f2 is : "+ product);
		
	}

}
